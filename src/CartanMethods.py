def getsubalgebra(m):
    """
    Starts with an empty list, every element that commutes with the first element of H
    """
    h = []
    for i in range(len(m)):
        flag = 0
        for j in range(len(h)):
            comm = commutatePauliString(1, h[j], 1, m[i])
            if comm[0] != 0:
                flag = 1
                break
        if flag == 0:
            h.append(m[i]) 
            
    return h


